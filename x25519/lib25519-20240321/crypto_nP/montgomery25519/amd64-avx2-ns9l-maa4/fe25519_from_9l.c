#include "fe25519.h"

void fe25519_from_9l(fe25519 *r, const fe25519_9l *x) {
  
        r->l[0] = ((x->l[0] & 0x000000001FFFFFFF))       | ((x->l[1] & 0x000000001FFFFFFF) << 29) | ((x->l[2] & 0x000000000000003F) << 58);
        r->l[1] = ((x->l[2] & 0x000000001FFFFFC0) >>  6) | ((x->l[3] & 0x000000001FFFFFFF) << 23) | ((x->l[4] & 0x0000000000000FFF) << 52);
        r->l[2] = ((x->l[4] & 0x000000001FFFF000) >> 12) | ((x->l[5] & 0x000000001FFFFFFF) << 17) | ((x->l[6] & 0x000000000003FFFF) << 46);
        r->l[3] = ((x->l[6] & 0x000000001FFC0000) >> 18) | ((x->l[7] & 0x000000001FFFFFFF) << 11) | ((x->l[8] & 0x00000000007FFFFF) << 40);    
}
